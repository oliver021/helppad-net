namespace Helppad
{
    /// <summary>
    /// The standard formats for usage in general
    /// </summary>
    public static class StandardFormat
    {
        /// <summary>
        /// The default format for conventional string.
        /// </summary>
        public const string Default = "G";

        /// <summary>
        /// The basic standard for numbers.
        /// </summary>
        public const string Number = "N";

        /// <summary>
        /// The basic standard for percent.
        /// </summary>
        public const string Percent = "P";

        /// <summary>
        /// The basic standard for round trip.
        /// </summary>
        public const string RoundTrip = "R";

        /// <summary>
        /// The basic standard for HexLarge.
        /// </summary>
        public const string HexLarge = "x2";

        /// <summary>
        /// The basic standard for gexadecimal.
        /// </summary>
        public const string Hex = "x";

        /// <summary>
        /// The basic standard for HexUpperCase.
        /// </summary>
        public const string HexUpperCase = "X";

        /// <summary>
        /// The basic standard for concurrency.
        /// </summary>
        public const string Concurrency = "C";

        /// <summary>
        /// The basic standard for fixed point.
        /// </summary>
        public const string FixedPoint = "F";
    }
}
